static unsigned short wavaData[248];

wave::wave(QWidget *parent) : QWidget(parent)
{
    // 0. 判断数据库是否已经添加

    // 1.加载数据库驱动
    QSqlDatabase db = QSqlDatabase::addDatabase("QSQLITE", "device_connection");

    // 2. 设置数据库名
    db.setDatabaseName("F:/Qt Project/test.db");

    // 3. 打开数据
    if (!db.open())
    {
        return;
    }

    // 4.准备数据
    QByteArray ba;
    unsigned char ch;
    for(int i=0; i<248; i++)
    {
        ch = (Spo2_DemoData[i] >> 8) & 0xff;
        ba.append(ch);  // 高字节
        ch = Spo2_DemoData[i] & 0xff;
        ba.append(ch);  // 低字节
    }

    // 5.执行插入语句
    QSqlQuery query(db);
    QString sql = "insert into sample values(null, 1, :wavaData, datetime('now', 'localtime'));";
    query.prepare(sql);
    query.bindValue(":wavaData", QVariant(ba));
    if (query.exec())
    {
        qDebug() << "插入成功";
    }
    else
    {
        qDebug() << "插入失败";
    }

    // 6.读取
    sql = "select * from sample where sampleId='4';";
    query.exec(sql);
//    QByteArray ba;
    while(query.next())
    {
        qDebug() << query.value(0).toInt();
        qDebug() << query.value(1).toInt();
        ba = query.value(2).toByteArray();
        qDebug() << query.value(3).toDateTime().toString("yyyy-MM-dd HH:mm:ss");
    }

    unsigned short usValue = 0;
    int cnt = 0;
    for(int i=0; i<ba.size(); i+=2)
    {
        usValue = 0;
        ch = ba.at(i);
        usValue |= (ch << 8);  // 高字节
        ch = ba.at(i+1);
        usValue |= ch&0xff;    // 低字节

        wavaData[cnt++] = usValue;  // 保存
    }
    qDebug() << "end..";



    this->resize(600,200);
    pix_= new QPixmap(this->width(),this->height());
    pix_->fill(Qt::black);

    QTimer* timer = new QTimer(this);
    connect(timer,SIGNAL(timeout()),this,SLOT(refreshPixmap()));
    timer->start(10);

}

void wave::refreshPixmap()
{
    static int pointX = 0;
    static int pointY = 0;
    static int direct = 0;

    QPainter painter(pix_);
    painter.setPen(QPen(Qt::white,5));

//    int *pecgWave


    int height = pix_->height();
    int width = pix_->width();
    direct += 1;
    pointX += 1;
    pointY = (wavaData[direct]);


    //1.2删除历史波形
    painter.save();
    painter.setPen(QPen(Qt::black,5));
    painter.drawLine(pointX+3, 0, pointX+3, height);
    painter.restore();

    //1.3 绘制
    painter.drawPoint(pointX,pointY);

    //1.4 一屏幕完成后，重新开始
    if (pointX >= width)
    {
        pointX = 0;
    }
    if (direct > 248)
    {
        direct = 0;
    }

    //2.更新
    this->update();
}

void wave::paintEvent(QPaintEvent *event)
{
    QPainter painter(this);
    painter.drawPixmap(10, 10, pix_->width(), pix_->height(), *pix_);
}
